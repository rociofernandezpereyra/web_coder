                <div class="negro">
                  <p class=" lead gsap-reveal-hero">El rol del productor en un disco, a mi entender tiene diferentes aristas pero con un sólo objetivo:
                  guiar a los músicos a llevar adelante una grabación.
                  Personalmente me identifico con el rol del Productor/Técnico de grabación, ya sea por mi formación o
                  por mi experiencia en discos grabados.</p>
                  <p class=" lead gsap-reveal-hero">Producir es llevar a cabo. Producir es aprender y hacer (y porque no aprender haciendo).</p>
                  <p class=" lead gsap-reveal-hero">Producir discos es, en gran parte, conocer los pasos a seguir en el proceso de grabación y al mismo
                  tiempo trabajar con el factor humano y las emociones que tiene la música.</p>
                  <p class=" lead gsap-reveal-hero">Es por eso que en mis trabajos de productor me involucro con el proyecto desde la instancia de
                  pre-producción hasta la post-producción, poniendo a disposición todo mi ser.</p>
                  <p class=" lead gsap-reveal-hero">Desde que montamos LA FABRICA DE MUSICA, aquel primer estudio en la ciudad de Buenos Aires, que no
                  pasó un año en mi vida sin llevar a cabo al menos una producción discográfica. Me siento cómodo en
                  diversos géneros y he tenido la fortuna de trabajar en los estudios más prestigiosos del país.</p>
                  <p class=" lead gsap-reveal-hero">Actualmente, me hallo produciendo a distancia, con los desafíos que eso conlleva.</p>
                  <p class=" lead gsap-reveal-hero">Al igual que en las clases on-line se utilizan todas las herramientas de comunicación necesarias para
                  dicha empresa. Hoy hago base en ESTUDIO TILTER, pero también grabo en otros estudios de Mar del Plata
                  y Buenos Aires.</p>
                  <p class=" lead gsap-reveal-hero">La metodología de trabajo de producción está basada en el diálogo grupal. Con la escucha de
                  referencias musicales, evaluación de los medios de realización, la puesta en acción y finalmente el
                  cierre con el mastering.</p>
                  <p class=" lead gsap-reveal-hero">Entiendo que la tarea en equipo es la mejor y más eficaz herramienta, es por eso que en casi todos mis
                  trabajos, me valgo de distintos colegas (músicos, ingenieros y técnicos) para fines específicos en
                  cada etapa.</p>
